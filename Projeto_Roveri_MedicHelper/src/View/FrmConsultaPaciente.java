    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel m = new DefaultTableModel();
            m.setRowCount(0);
            m.addColumn("Nome Paciente");
            m.addColumn("Nome Medico");
            m.addColumn("Data da consulta");
           

            CtrlListarConsulta consulta = new CtrlListarConsulta();
            ArrayList<Consulta> lista = consulta.calendarios();

            for (Consulta consullist : lista) {
                m.addRow(new Object[]{consullist.getID_Paciente(), consullist.getID_Medico(), consullist.getDataconsulta()});
            
            }
            Lista.setModel(m);
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

