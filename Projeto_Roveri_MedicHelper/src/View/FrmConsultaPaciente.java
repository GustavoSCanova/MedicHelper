    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int resposta = 0;
        
        String ID_Medico = Lista.getValueAt(Lista.getSelectedRow(), 0).toString();
        String id = lblUser_ID.getText();
       
        resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja Realmente Fazer esse Cadastro ?");
        if (resposta == JOptionPane.YES_OPTION) {
            CtrlConsultaPaciente CtrCadastro = new CtrlConsultaPaciente();
            Consulta consulta = new Consulta();
            String  data  = cmbano.getSelectedItem() + "-" + cmbmes.getSelectedItem() + "-" + cmbdia.getSelectedItem(); 
            consulta.setID_Paciente(id);
            consulta.setID_Medico(ID_Medico);
            consulta.setDataconsulta(data);
            
            try {
                JOptionPane.showMessageDialog(null, CtrCadastro.ConsultaPac(consulta));
            } catch (Exception ex) {
                Logger.getLogger(FrmConsultaPaciente.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

