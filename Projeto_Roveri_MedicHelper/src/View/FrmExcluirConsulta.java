    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int resposta = 0;

        
        
        String ID_Consulta =   (Lista.getValueAt(Lista.getSelectedRow(), 0).toString());
        
        resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja Realmente Excluir esse Cadastrado ?");
        if (resposta == JOptionPane.YES_OPTION) {
            CtrlExcluirConsulta CtrDel = new CtrlExcluirConsulta();

            Consulta excpac = new Consulta();
            
            
            excpac.setID_Consulta(ID_Consulta);
            
        
            try {
                JOptionPane.showMessageDialog(null, CtrDel.expcalendar(excpac));

            } catch (Exception ex) {
                Logger.getLogger(CtrlExcluirConsulta.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

