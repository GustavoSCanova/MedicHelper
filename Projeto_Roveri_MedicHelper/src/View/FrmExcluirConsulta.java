    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel m = new DefaultTableModel();
            m.setRowCount(0);
            m.addColumn("ID_Consulta");
            m.addColumn("Nome Paciente");
            m.addColumn("Nome Medico");
            m.addColumn("Data da consulta");

            CtrlListarConsulta consulta1 = new CtrlListarConsulta();
            ArrayList<Consulta> listas = consulta1.lista();

            for (Consulta consullist : listas) {
                m.addRow(new Object[]{consullist.getID_Consulta(),consullist.getID_Paciente(), consullist.getID_Medico(), consullist.getDataconsulta()});

            }
            Lista.setModel(m);
        } catch (Exception ex) {

        }
        
        txtnomepaciente.setText(null);
        txtnomemedico.setText(null);
        txtdataconsulta.setText(null);
        
    }//GEN-LAST:event_jButton1ActionPerformed

